class CartInfo{


    product

    quantity


    constructor(product, quantity)
    {
        this.product = product;
        this.quantity = quantity;
    }

    getProduct()
    {
        return this.product;
    }
    setProduct(product)
    {
        this.product = product;
    }
    getQuantity()
    {
        return this.quantity;
    }

    setQuantity(quantity)
    {
        this.quantity = quantity;
    }

}